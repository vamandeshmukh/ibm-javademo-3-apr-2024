package com.ibm.day1;

public class OperatorsDemo {
	
	public static void main(String[] args) {
		
		int num = 10;
		
		System.out.println(num); // 10
		System.out.println(num++); // 10 
		System.out.println(++num); // 12
		System.out.println(num--); // 12
		System.out.println(--num); // 10
		
	}

}
